def product(a, b):
    """Return the product of a and b.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The product of a and b.
    """
    return a * b

# Test cases
print(product(2, 2))   # Output: 4
print(product(2, -2))  # Output: -4
